# 4. Given the string s = 'hello', perform the following operations:
s = "hello"

#Convert the string to uppercase.
print("Uppercase:", s.upper())
#Replace 'e' with 'a'.
print("Replace e with a:", s.replace("e", "a"))
#Check if the string starts with 'he'.
print("Starts with 'he':", s.startswith("he"))
#Check if the string ends with 'lo'.
print("Ends with 'lo':", s.endswith("lo"))