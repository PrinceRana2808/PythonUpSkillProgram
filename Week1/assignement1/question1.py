# 1.Convert the following values to the specified types and print the results

# Convert 3.75 to integer
a = 3.75
int_value = int(a)
print("Convert 3.75 to int:", int_value)

# Convert "123" to float
b = "123"
float_value = float(b)
print("Convert '123' to float:", float_value)

# Convert 0 to boolean
c = 0
bool_value = bool(c)
print("Convert 0 to boolean:", bool_value)

# Convert False to string
d = False
str_value = str(d)
print("Convert False to string:", str_value)